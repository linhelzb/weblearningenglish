package BEAN;

public class HuongDanHocTuVung {
	private int ma_loai_tu_vung;
	private String ten_nhom_tu_vung;
	private String hinh_anh;
	private int kiem_tra_noi_dung;
	public int getMa_loai_tu_vung() {
		return ma_loai_tu_vung;
	}
	public void setMa_loai_tu_vung(int ma_loai_tu_vung) {
		this.ma_loai_tu_vung = ma_loai_tu_vung;
	}
	public String getTen_nhom_tu_vung() {
		return ten_nhom_tu_vung;
	}
	public void setTen_nhom_tu_vung(String ten_nhom_tu_vung) {
		this.ten_nhom_tu_vung = ten_nhom_tu_vung;
	}
	public String getHinh_anh() {
		return hinh_anh;
	}
	public void setHinh_anh(String hinh_anh) {
		this.hinh_anh = hinh_anh;
	}
	public int getKiem_tra_noi_dung() {
		return kiem_tra_noi_dung;
	}
	public void setKiem_tra_noi_dung(int kiem_tra_noi_dung) {
		this.kiem_tra_noi_dung = kiem_tra_noi_dung;
	}
	
	
}
