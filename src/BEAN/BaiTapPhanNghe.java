package BEAN;

public class BaiTapPhanNghe {
	private int ma_bai_tap_nghe;
	private String ten_bai_tap_nghe;
	private String anh_bai_tap_nghe;
	private int kiem_tra_cau_hoi;
	public int getMa_bai_tap_nghe() {
		return ma_bai_tap_nghe;
	}
	public void setMa_bai_tap_nghe(int ma_bai_tap_nghe) {
		this.ma_bai_tap_nghe = ma_bai_tap_nghe;
	}
	public String getTen_bai_tap_nghe() {
		return ten_bai_tap_nghe;
	}
	public void setTen_bai_tap_nghe(String ten_bai_tap_nghe) {
		this.ten_bai_tap_nghe = ten_bai_tap_nghe;
	}
	public String getAnh_bai_tap_nghe() {
		return anh_bai_tap_nghe;
	}
	public void setAnh_bai_tap_nghe(String anh_bai_tap_nghe) {
		this.anh_bai_tap_nghe = anh_bai_tap_nghe;
	}
	public int getKiem_tra_cau_hoi() {
		return kiem_tra_cau_hoi;
	}
	public void setKiem_tra_cau_hoi(int kiem_tra_cau_hoi) {
		this.kiem_tra_cau_hoi = kiem_tra_cau_hoi;
	}
	
	
}
