package BEAN;

public class ThanhVien {
	private int ma_thanh_vien;
	private String ten_day_du;
	private String ten_thanh_vien;
	private String mat_khau;
	private String loai_thanh_vien;
	public int getMa_thanh_vien() {
		return ma_thanh_vien;
	}
	public void setMa_thanh_vien(int ma_thanh_vien) {
		this.ma_thanh_vien = ma_thanh_vien;
	}
	public String getTen_day_du() {
		return ten_day_du;
	}
	public void setTen_day_du(String ten_day_du) {
		this.ten_day_du = ten_day_du;
	}
	public String getTen_thanh_vien() {
		return ten_thanh_vien;
	}
	public void setTen_thanh_vien(String ten_thanh_vien) {
		this.ten_thanh_vien = ten_thanh_vien;
	}
	public String getMat_khau() {
		return mat_khau;
	}
	public void setMat_khau(String mat_khau) {
		this.mat_khau = mat_khau;
	}
	public String getLoai_thanh_vien() {
		return loai_thanh_vien;
	}
	public void setLoai_thanh_vien(String loai_thanh_vien) {
		this.loai_thanh_vien = loai_thanh_vien;
	}
	
}
